    <h1>Syllabus</h1>
    <h3>1. Introduction and Data Communication Fundamentals</h3>
    <ul>
        <li>Introduction to computer networks</li>
        <li>Classification of computer networks</li>
        <li>Transmission media : guided and unguided media.</li>
        <li>
            Functions of network connecting devices: Amplifier, Repeater,
            Bridge, Hub, Switch, Router, Gateway, Modems
        </li>
        <li>
            Data transmission concepts : transmission modes, multiplexing,
            switching technologies, asynchronous and synchronous transmission
        </li>
        <li>Introduction to Local Area Networks (LANs), LAN topologies,</li>
        <li>Gigabit Ethernet</li>
    </ul>
    <h3>2. Layered Protocols</h3>
    <ul>
        <li>Protocols, Protocol hierarchies</li>
        <li>Design issues for the layers</li>
        <li>The OSI reference model and the TCP/IP reference model</li>
        <li>The Internet Protocol (IP), IP addresses, Subnets,</li>
        <li>
            Introduction to Transmission Control Protocol (TCP), The TCP segment
            header
        </li>
        <li>Introduction to User Datagram Protocol (UDP)</li>
    </ul>
    <h3>3. Routing, Congestion Control and Internetworking</h3>
    <ul>
        <li>Virtual Circuits and Datagrams</li>
        <li>Routing Techniques</li>
        <li>Congestion Control</li>
        <li>Introduction and Issues that arise in Internetworking</li>
        <li>Fragmentation</li>
        <li>Tunneling</li>
        <li>Virtual Private Networks</li>
    </ul>
    <h3>4. Wireless Communication and Network Security</h3>
    <ul>
        <li>Introduction to Wireless Networks</li>
        <li>Satellite Communication (LEO, MEO, GEO)</li>
        <li>Wireless LAN protocols</li>
        <li>Introduction to Mobile Telephone Systems, Cell Fundamentals</li>
        <li>
            Traditional Cryptography, Substitution Cipher vs Transposition
            Cipher, Fundamental Cryptographic Principles
        </li>
        <li>Secret-Key Algorithms vs Public-Key Algorithms</li>
        <li>Firewalls</li>
    </ul>
